#include "libft.h"

void	ft_putnbr(int n)
{
	long int l;

	l = n;
	if (l < 0)
	{
		l *= -1;
		ft_putchar('-');
	}
	if (l > 9)
	{
		ft_putnbr(l / 10);
		ft_putchar(l % 10 + '0');
	}
	else
		ft_putchar(l + '0');
}
